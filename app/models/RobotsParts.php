<?php namespace Invo\Models;

use Phalcon\Mvc\MongoCollection;

class RobotsParts extends MongoCollection
{
    // ------------------------------------------------------------------------------
    
    public $_id;
    
    // ------------------------------------------------------------------------------
    
    public $robots_id;

    // ------------------------------------------------------------------------------

    public $parts_id;

    // ------------------------------------------------------------------------------

    public $created_at;

    // ------------------------------------------------------------------------------

    public function initialize()
    {
        $this->belongsTo(
            "robots_id",
            "Robots",
            "id"
        );

        $this->belongsTo(
            "parts_id",
            "Parts",
            "id"
        );
    }

    // ------------------------------------------------------------------------------
}